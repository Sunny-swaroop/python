#"sep"
#function to specify the separator between the values that are being printed.
#By default, print() separates the values with a space, but you can change this behavior using the sep parameter.
print("apple""banana""cherry")
       #now it will print (applebananacherry) with no spaces in between them
print("apple","banana","cherry")
    #now we are giving comma in between them, it will print space between them
print("apple","banana","cherry",sep=",")
    #now it will print comma between them (apple, banana,cherry)



